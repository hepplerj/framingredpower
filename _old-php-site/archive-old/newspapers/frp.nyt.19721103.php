                <h2>500 Indians Seize U.S. Building After Scuffle With Capital Police</h2>
                <p><span title="Occupation: NumberOfActivists" style="background-color: transparent;">About 500 American Indians</span> <span title="Occupation: Purpose" style="background-color: transparent;">protesting injustices</span>, took control tonight of the Bureau of Indian Affairs building after scuffles with the police outside and inside the building.</p>

				<p>Late tonight, <span title="Reactions: NixonAdministration" style="background-color: transparent;">Nixon Administration officials were apparently prepared to let the Indians bivouac in the building until morning.  Negotiations between Indian leaders and Administration officials continued without resolution.</span></p>

				<p><span title="Occupation: Women" style="background-color: transparent;">Women and about two dozen children were bedded on the third floor of the building, which the Indians scaled off.</span>  Many of the demonstrators had brought bedrolls and sleeping bags.</p>

				<p><span title="Occupation: Conflict" style="background-color: transparent;"><span title="Reactions: LawEnforcement" style="background-color: transparent;">A force of about 150 policemen surrounded the building at the closing hour, but Department of the Interior officials held the policemen back as young activist Indians barricaded themselves inside the building by piling furniture against doors.</span></span></p>

				<p><span title="Reactions: LawEnforcement" style="background-color: transparent;">No arrests were made.</span>  Meanwhile, leaders of the &quot;Trail of Broken Treaties,&quot; <span title="Occupation: Purpose" style="background-color: transparent;">a caravan of Indians bent on emphasizing the rights of Indians as the first Americans</span>, sought to arrange meetings with Administration officials to solve the impasse over their demands for housing and food during their march on Washington.</p>

				<p>The sole major Indian bureau official within the building appeared to be John O. Crow, associate commissioner of Indian affairs.</p>

				<p><span title="Reactions: BIAOfficials" style="background-color: transparent;">Mr. Crow said that after the first scuffles outside the building, the situation was "calm" as the 500 Indian protesters awaited word on whether they would gain a place to sleep and quarters from which to conduct further negotiations.</span></p>

				<p><strong>Furniture Broken</strong></p>

				<p><span title="Occupation: DamageToBuilding" style="background-color: transparent;">During the melee, windows were broken and furniture was ripped apart to provide the militant Indians with clubs.  Carl Shaw, an information officer, said that some duplicating machines were overturned and papers from officers of the information section were scattered through the halls.</span></p>

				<p>Mr. Shaw said that he and others from the information office were forced to elave the building through a window on the basement level.  He said that all calls to his office were taken over by the protesters.</p>

				<p><span title="Individuals: LouisBruce" style="background-color: transparent;">Louis R. Bruce, the Indian Affairs Commissioner, said that he and other officials had decided that the protesters would be permitted to stay in the building tonight and expressed hope that all problems could beresolved tomorrow.</p>

				<p>"We can work with them," said Mr. Bruce, a Mohawk and Ogalla Sioux from New York, after meeting late tonight with the leaders of the militants.</span></p>

				<p><span title="Occupation: Leadership" style="background-color: transparent;">The protesters were led by members of the American Indian Movement, a militant group that has been in the forefront of Indian groups demanding more aid from the Nixon Administration for Indian economic and social needs.</span></p>

				<p><span title="Reactions: NixonAdministration" style="background-color: transparent;">Leaders of the protest met tonight with various officials including Bradford Patterson, a White House assistant for minority affairs.  Mr. Patterson earlier had made public letters to the Indian leaders, telling them that President Nixon was unable to meet with them but that the Administration would assist them in establishing a "meaningful dialogue."</span></p>

				<p>Newsmen seeking entrance to the building late tonight were turned away by the police.  Some newsmen within the building were told that they could not leave.</p>

				<p>Telephone calls to various offices were answered by Indians who identified themsleves as part of the invading group.</p>

				<p>The Bureau of Indian Affairs said that its offices would be closed tomorrow while the negotiations continued.</p>

				<p>"Answering the 'phone and telling you that the Pigs are all around us," was the response.</p>

				<p><span title="Occupation: Activists" style="background-color: transparent;">Earlier, Mrs. Martha Gras, a 71-year-old Pawnee Indian, appeared to sum up the sentiment of the Indian gathering.</span></p>

				<p><span title="Occupation: Activists" style="background-color: transparent;">&quot;There are nothing but crooks and liars up here,&quot; she shouted at cheering Indians in an auditorium in the building.  &quot;They'll steal you blind.&quot;</span></p>

				<p><span title="Occupation: Activists" style="background-color: transparent;">Martha, as she appeared to be widely known to &quot;The Trail of Broken Treaties&quot; caravan, drew cries of &quot;Right on&quot; as she sought to detail injustices the Indians had suffered from white men.</span></p>

				<p><span title="Occupation: NumberOfActivists" style="background-color: transparent;">More than 500 Indians came to the nation's capital</span> <span title="Occupation: Purpose" style="background-color: transparent;"><span title="Context: TrailOfBrokenTreaties" style="background-color: transparent;">for a week-long protest against broken treaties and failure of the Government to carry out trust responsibilities to the original Americas.  They abandoned a program laid out by their leaders and gathered in the auditorium of the Bureau of Indian Affairs at the Department of the Interior.</span></span></p>

				<p><span title="Occupation: Conflict" style="background-color: transparent;">The scheduled program broke down because of a lack of housing, food and funds that the Indians, mainly young activists, had expected to find there.  They protested that the Government had failed to assist them and that the first caravan contingent, which arrived here last night, had to spend the night in a "rat-infested" church that had served minority groups in past demonstrations.</p></p>

				<p><strong>'Listening for 300 Years'</strong></p>

				<p>They vowed to stay in the Indian Affairs Bureau until the Government provided adequate facilities.  <span title="Reactions: InteriorOfficials" style="background-color: transparent;">Harrison E. Loesch, Assistant Secretary of the Interior, and other Administration officials promised to "explore" every possible avenue to give them housing and other facilities for their stay.</span></p>

				<p><span title="Reactions: InteriorOfficials" style="background-color: transparent;">Mr. Loesch sought to assure the Indians that the Nixon Administration was dedicated to meeting their economic and social needs.  He cited budget increases and other measures designed to aid Indians in education and in water and fishing rights as evidence of the Administration's good will.</span></p>

				<p><span title="Occupation: Activists" style="background-color: transparent;">The Indians shouted back at him, &quot;Harrison, you don't know anything about Indian affairs.&quot;  Others demanded: &quot;You should turn the bureau over to us; we should be over you.&quot; and &quot;We've been listening to you for 300 years.&quot;</span></p>

				<p>Nearly forgotten in the heated, confused meeting with Mr. Loesch and other Government officials was a <span title="Occupation: Purpose" style="background-color: transparent;">20-point program drawn up by a group of Indians who had started from the West Coast and who visited various Western reservations for workshops on the way to Washington.</span></p>

				<p><span title="Occupation: Purpose" style="background-color: transparent;">The Indians' proposals called for the Government to make mandatory the judicial enforcement of their existing treaty rights, for restoration of lands taken from Indians in earlier years, and for elimination of &quot;inequities&quot; in ownership or control of national resources by which, the Indians contend, white men control their lands.</span></p>

				<p><span title="Context: TrailOfBrokenTreaties" style="background-color: transparent;">The Indians had arrived here on buses, station wagons and other vehicles, packed with sleeping gear and other necessities.</span></p>

