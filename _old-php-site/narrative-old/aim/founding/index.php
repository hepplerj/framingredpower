            	<h2>Founding Years</h2>
        		<p>The events of 1968 shaped the attitudes and ideas of AIM's founders.  In the context of these new ideas, the questions of an Indian's place in American society gained overwhelming significance.  The vast evidence of injustice, as they saw it, proved a recurring pattern in history regarding the subjugation of Indians and a legacy of broken laws and promises.  Reinforcement for this belief was manifest in disillusionment within the urban centers where many were pushed to under the government’s relocation policy, causing many to loose their roots in culture and tradition.  While serving a nine-month prison sentence, future AIM co-founder Dennis Banks occupied his time by reading voraciously about the Civil Rights Movement, antiwar movement, and Indian treaties.  Watching the progress of the African American and student rebellions, he realized &quot;there was a hell of a goddamn movement going on that I wasn't part of. . . [and saw] the greatest war was going to go on right here in the United States.&quot;   Upon his release from prison, Banks sought to make a profound and lasting impact on Indian politics and constructing a movement served as the best vehicle for that change.</p>
                
                <p>In the beginning, AIM modeled itself after the Black Panther Party's community self-defense patrol in Oakland, California.   Hoping to curb police brutality and the disproportionate number of Indian arrest, AIM activists devised strategies to monitor police activity.  They patrolled Minneapolis's Franklin Avenue armed with walkie talkies and donned red leather jackets that read &quot;American Indian Movement&quot; across the back.  More often than not, they also rode around in red Cadillac convertibles.  If a patrol came across Indians facing arrests, they would videotape the whole affair.  They later began offering rides home to Indians at bars, catching them before the police could, or bailing Indians out of jail.  Before long, AIM as a local organization had grown to a place where Indians could come and receive help in finding a job, education, or get a loan.  The poor housing and education, joblessness, and general lack of respect for Native Americans reached its tipping point as Indian pride hit full bloom. </p>
                
                <p>A large part of their change in thinking occurred when a group calling themselves Indians of All Tribes (IAT) occupied Alcatraz Island in 1969.  Headed by Mohawk Indian Richard Oakes and Santee Dakota John Trudell (later to become AIM's national chairman), the group arrived on the island and demanded title to the location with plans to establish a Center for Native American Studies, an American Indian Spiritual Center, an Indian Center of Ecology, a Great Indian Training School, and an American Indian Museum.  Some members of the American Indian Movement participated in the occupation, and the lesson was not lost on them.  The publicity of the event convinced many that confrontational politics was a successful form of political protest.</p>


