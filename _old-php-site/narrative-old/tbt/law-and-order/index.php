            	<h2>Identity, Militancy, and the Politics of Law and Order</h2>
        		<p>Street crime, riots, and demonstrations fueled new political discussions in the 1960s that eventually came to hurt political progressives.  Civil disobedience, Supreme Court decisions like Escobedo and Miranda, and the perception of President Lyndon Johnson's Great Society rewarding participants of urban riots drove anxious voters—of all races—away from liberals and toward conservatives, who offered new ideas for combating violence and disorder.  The loss of faith in liberalism protecting an individual's personal security dramatically transformed American politics, destabilizing the Great Society and contributing to President Richard Nixon's victory over Hubert Humphrey.  Nearly twelve million voters abandoned the Democratic Party between 1964 and 1968, in part because it failed to address the issue of social chaos.</p>
                
				<p>Republican presidential candidate Barry Goldwater raised the issue of law and order during the 1964 campaign against Johnson. During the March New Hampshire primary, Goldwater demanded that Johnson provide the &quot;lights of law and order.&quot;  &quot;Government seeks to be parent, teacher, leader, doctor, and even minister,&quot; Goldwater charged.  &quot;And its failures are strewn about us in the rubble of rising crime rates.&quot;   By connecting liberalism to the source of civil unrest, he tied together disorder with liberal ideas as twin threats to American society.  As the decade marched onward, the logic gained wider acceptance by the general population.  Fueling the unease over the direction of social change was mass media and their depiction of violent crime and political demonstrations.  These ideas and attitudes extended into the 1970s.</p>
    
				<p>The major news outlets first discovered the American Indian Movement in 1970, two years after its founding in Minneapolis. CBS first reported on AIM's police patrols in June, which was their attempt to curb police harassment by monitoring and videotaping the actions of law enforcement. As AIM began national campaigns, they staged larger protests and developed relationships with reporters. Members of AIM invited reporters to their Thanksgiving Day protest in 1970, where they buried Plymouth Rock in sand and seized the <em>Mayflower II</em> replica. Jim Hale with ABC and John Chancellor of NBC were present during the Thanksgiving protest, as was a reporter with the <em>New York Times</em>.  AIM hoped to present these symbols as oppressive and colonial, thus challenging Americans to consider what they were celebrating.  Throughout the summer and fall of the following year, a scattering of news stories followed AIM's protest activities, but the media never fully explored the reasons or message of this new form of Indian dissent. What the press chose to cover they framed as black-and-white issues of law versus lawlessness and Indian versus the government.</p>


