			<div class="scope">
			<p>
			The Trail of Broken Treaties caravan was meant to draw attention to issues facing Native Americans, but the press did 
not bother with coverage of the Trail until the occupation of the Bureau of Indian Affairs at the beginning of November 1972.  White and Native newspapers, television, government reports, and others all interpreted the Trail and occupation differently in public discourse,  giving meaning to the ways newsmakers presented the event and portrayed the activists.  This archive focuses on bringing together historical materials related to the Trail and occupation of the BIA and allows readers to explore news reports and develop their own understanding of the ways the Trail was reported on, ignored, contextualized, or 
interpreted by the press.</p> 
			<p>The early stages of this archive focused on newspaper coverage at the national, regional and local level but has expanded to encompass all news forms from newspapers, television, and news magazines, as well as contextual documents such as government reports, activist documents, speeches, treaties, speeches, and books that exist to help give context to events and attitudes. As the project develops, additional forms of representation, interpretation, and narrative will be added to fill out the archive.</p> 
