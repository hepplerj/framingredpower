                <h2>Analysis</h2>
        		<p>In the postwar decades both Native Americans and politicians became dissatisfied with the colonial-type relationship between Indians and the federal government.  Growing white guilt on the Left and the desire of the Right to end what they perceived as the collectivist and socialistic nature of reservations intersected to begin dismantling the reservation system. The Burea of Indian Affair's relocation program and federal government's termination policy both worked to deconstruct the reservations and relocate American Indians to urban centers with the intent of improving their economic standing and integrate them into "mainstream" society.  Between 1930 and 1970, nearly 118,000 Indians left the reservations for major relocation centers in Oakland, San Francisco, San Jose, Minneapolis, and St. Louis.</p>

<p>Media coverage of the activists focuses more on the occupation and federal property than the agenda of the activists <span class="mapExtLink">More</span><span class="mapMenuWrap"><span class="mapMenu" style="display: none; font-size: 0.9em;"><a href="http://segonku.unl.edu/~jheppler/sources/cloudnatl.htm">National Newspaper Word Cloud</a></span></span>.  The decision to occupy the BIA doomed the chance for the activists to engage in a dialogue about the Indian-federal relationship, instead turning media attention to the spectacle rather than the message.</p>

<p>The debate between integration and separation continued into the 1970s and became a central critique by the Caravan activists.  Media interest in the Trail of Broken Treaties gave Native voice to the debate and allowed them to present an unvarnished message to the public.  Integrating Native voice into the debate had the potential to shape public policy on Indian affairs.  One of the goals of the Caravan activists was calling for the abolition of the Burea of Indian Affairs and a new system for the Indian-federal relationship<span class="mapExtLink">More</span><span class="mapMenuWrap"><span class="mapMenu" style="display: none; font-size: 0.9em;">- <a href="http://segonku.unl.edu/~jheppler/archive/other/frp.tbt.19721027.xml">Twenty Points Position Paper</a><br>- <a href="http://segonku.unl.edu/~jheppler/archive/newspapers/frp.nyt.19721031.xml"><em>New York Times</em></a>, Oct. 31, 1972: "The caravans are an extension of the demonstrations by young activist Indians against the Bureau of Indian Affairs, an agency of the Department of the Interior."<br>- <a href="http://segonku.unl.edu/~jheppler/archive/newspapers/frp.minntrib.19721107.xml"><em>Minneapolis Tribune</em></a>, Nov. 7, 1972: "These Include demands that treaties be respected, that the BIA be abolished and -- in apparent contradiction -- that $30 million be restored to the BIA budget."</span></span>. </p>

<p>Between the end of September and the end of October print media took little interest in the Trail of Broken Treaties caravan as it traveled eastward.  The <em>New York Times</em> and the <em>Washington Post</em> ran only two stories prior to the caravan's arrival at the end of October <span class="mapExtLink">More</span><span class="mapMenuWrap"><span class="mapMenu" style="display: none; font-size: 0.9em;"><a href="http://segonku.unl.edu/~jheppler/sources/timeline.htm">Newspaper Timeline</a></span></span>.  The stories that appear are short pieces that refrain from detailed analysis or space to let the activists explain their goals.</p>
