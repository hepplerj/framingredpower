<p>In addition to greater coverage among mainstream news sources, a nationalistic Native American news media developed in the 1960s and 1970s, offering Native Americans a voice on current events.  Three major Native newspapers were founded in the 1960s and 1970s and brought tribal and urban news to a wide audience.  <em>Indian Voices</em>, published in Chicago from 1962 to 1968, <em>Akwesasne Notes</em>, published by the Mohawk Nation since 1968, and <em>Wassaja</em>, published irregularly since 1973, offered a mix of news writing and editorials on Native issues.  In 1970 Yakima journalist Richard La Course, along with Oglala Lakota Charles Trimble, Hopi Rose Robinson, among others, led the American Indian Press Association (AIPA) until its demise in 1976.  At one point AIPA provided more than 150 Indian newspapers and other sources with news.</p>

<p>The transformations in media coverage led to two trends.  The first was a more visible presence of Native American issues in the public discourse.  Matters of public policy as it concerned Native Americans were discussed widely and sympathetically by media sources.  Through the public attention that Red Power activism drew, Congressional leaders engaged Indian affairs, passed several bills and resolutions favorable to Native issues, and eventually embraced tribal self-determination <span class="mapExtLink">More</span><span class="mapMenuWrap"><span class="mapMenu" style="display: none; font-size: 0.9em;">
